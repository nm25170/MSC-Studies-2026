def fetch_london_covid_data():
    """Fetch London COVID-19 data from the UK Government API."""
    all_data = []
    page = 1

    print("Attempting to download London COVID-19 data from UK Government API...")

    while True:
        params = {
            "filters": ";".join(FILTERS),
            "structure": json.dumps(STRUCTURE),
            "page": page
        }

        response = requests.get(API_URL, params=params, timeout=30)

        if response.status_code == 204:
            break
        elif response.status_code != 200:
            raise Exception(f"API error {response.status_code}: {response.text}")

        payload = response.json()
        data = payload.get("data", [])

        if not data:
            break

        all_data.extend(data)
        print(f"  Page {page}: fetched {len(data)} records (total: {len(all_data)})")

        if page >= payload.get("pagination", {}).get("lastPage", 1):
            break

        page += 1

    return all_data


def load_sample_data():
    """Load the built-in sample London COVID-19 dataset."""
    print("Loading built-in sample London COVID-19 dataset...")
    df = pd.read_csv(io.StringIO(SAMPLE_CSV))
    return df


def main():
    output_file = "london_covid_data.csv"

    # Try live API first, fall back to sample data
    df = None
    try:
        raw_data = fetch_london_covid_data()
        if raw_data:
            df = pd.DataFrame(raw_data)
            print("Live data downloaded successfully.")
        else:
            print("API returned no data. Using sample dataset.")
            df = load_sample_data()
    except Exception as e:
        print(f"Could not reach API ({e}).")
        print("Using built-in sample dataset instead.")
        df = load_sample_data()

    # Parse and sort dates
    df["date"] = pd.to_datetime(df["date"])
    df = df.sort_values("date").reset_index(drop=True)

    # Save to CSV
    df.to_csv(output_file, index=False)
    print(f"\nDataset saved to: {os.path.abspath(output_file)}")

    # Summary
    print("\n--- Dataset Overview ---")
    print(f"Total records : {len(df)}")
    print(f"Date range    : {df['date'].min().date()} to {df['date'].max().date()}")
    print(f"Area          : {df['areaName'].iloc[0]}  (Code: {df['areaCode'].iloc[0]})")
    print(f"Columns       : {list(df.columns)}")
    print("\nFirst 5 rows:")
    print(df.head().to_string(index=False))
    print("\nBasic statistics:")
    print(df.describe().to_string())


if __name__ == "__main__":
    main()
